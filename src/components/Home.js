// Импортируем необходимые компоненты
import { Link } from 'react-router-dom'; // Компонент для навигации между страницами без перезагрузки
import { useAuth } from '../context/AuthContext'; // Кастомный хук для доступа к данным аутентификации

// Основной компонент домашней страницы
const Home = () => {
  // Получаем текущего пользователя из контекста аутентификации
  const { currentUser } = useAuth();

  // Возвращаем JSX разметку
  return (
    <div>
      <h1>Добро пожаловать</h1>
      
      {/* Условный рендеринг в зависимости от состояния аутентификации */}
      {currentUser ? (
        // Если пользователь авторизован
        <p>
          Вы вошли как {currentUser.email}. {/* Отображаем email пользователя */}
          <Link to="/profile">Перейти в профиль</Link> {/* Ссылка на профиль */}
        </p>
      ) : (
        // Если пользователь не авторизован
        <div>
          <p>
            Пожалуйста, {/* Предложение войти или зарегистрироваться */}
            <Link to="/signin">войдите</Link> {/* Ссылка на страницу входа */}
            или {/* Разделитель */}
            <Link to="/signup">зарегистрируйтесь</Link> {/* Ссылка на страницу регистрации */}
          </p>
        </div>
      )}
    </div>
  );
};

// Экспортируем компонент для использования в других частях приложения
export default Home;